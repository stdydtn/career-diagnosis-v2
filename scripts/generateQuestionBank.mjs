function difficultyForIndex(i, total) {
  const bEnd = Math.max(1, Math.floor(total * 0.4))
  const sEnd = Math.max(bEnd + 1, Math.floor(total * 0.8))
  if (i < bEnd) return 'basic'
  if (i < sEnd) return 'standard'
  return 'advanced'
}

function pad3(n) {
  return String(n).padStart(3, '0')
}

function buildFromSpecs(type, key, specs) {
  const m = META[type]
  return specs.map((row, idx) => ({
    id: `${type}_${key}_${pad3(idx + 1)}`,
    section: m.section,
    displaySection: m.displaySection,
    type: m.type,
    key,
    text: row.text,
    intent: row.intent,
    tags: row.tags,
    difficulty: difficultyForIndex(idx, specs.length),
  }))
}

